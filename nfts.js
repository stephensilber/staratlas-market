export function nftForMarket(market) {
  let matchingNfts = nfts.filter(
    (x) => x.markets.filter((y) => y.id == market).length > 0
  );
  if (matchingNfts.length == 0) {
    return null;
  } else {
    return matchingNfts[0];
  }
}

export function colorForRarity(rarity) {
  switch (rarity) {
    case "anomaly":
      return "red";
    case "epic":
      return "violet";
    case "legendary":
      return "yellow";
    case "common":
      return "green";
    case "rare":
      return "blue";
    default:
      return "white";
  }
}

export function sortOrderForRarity(rarity) {
  switch (rarity) {
    case "anomaly":
      return 6;
    case "legenary":
      return 5;
    case "epic":
      return 4;
    case "rare":
      return 3;
    case "uncommon":
      return 2;
    case "common":
      return 1;
    default:
      return 0;
  }
}

export function colorForItemType(itemType) {
  switch (itemType) {
    case "collectible":
      return "indigo";
    case "ship":
      return "teal";
    case "access":
      return "cyan";
    case "structure":
      return "blue";
    default:
      return "white";
  }
}

export function colorGradientForRarity(rarity) {
  switch (rarity) {
    case "anomly":
      return { from: "red", to: "pink", deg: 45 };
    case "legendary":
      return { from: "yellow", to: "orange", deg: 45 };
    case "epic":
      return { from: "indigo", to: "violet", deg: 45 };
    case "rare":
      return { from: "blue", to: "cyan", deg: 45 };
    case "uncommon":
      return { from: "green", to: "lime", deg: 45 };
    case "common":
      return { from: "white", to: "gray", deg: 45 };
    default:
      return { from: "white", to: "gray", deg: 45 };
  }
}
